// Add
function add() {
    const number1 = prompt('Enter the first summand', 0);
    const number2 = prompt('Enter the second summand', 0);

    alert(Number(number1) + Number(number2));
}

// Substract
function substract() {
    const number1 = prompt('Enter the substractor', 0);
    const number2 = prompt('Enter the deductible', 0);

    alert(Number(number1) - Number(number2));
}

// Multiply
function multiply() {
    const number1 = prompt('Enter the first summand', 0);
    const number2 = prompt('Enter the second summand', 0);

    alert(Number(number1) * Number(number2));
}

// Divide
function divide() {
    const number1 = prompt('Enter the first summand', 0);
    const number2 = prompt('Enter the second summand', 0);

    alert(Number(number1) / Number(number2));
}